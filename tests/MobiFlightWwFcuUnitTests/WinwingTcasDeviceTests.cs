        public void SetLed_SameStateTwice_OnlySendsOnce()
        {
            device.SetLed("ATC_FAIL", 1);
            device.SetLed("ATC_FAIL", 1);

            Assert.HasCount(1, mockMessageSender.LightControlCommands);
        }


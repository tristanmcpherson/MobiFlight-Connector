        [TestMethod]
        public void Stop_TurnsOffAtcFailLedAndSendsNoDisplayFrames()
        {
            device.Connect();
            device.SetLed("ATC_FAIL", 1);
            mockMessageSender.Reset();

            device.Stop();

            Assert.IsGreaterThan(0, mockMessageSender.LightControlCommands.Count);
